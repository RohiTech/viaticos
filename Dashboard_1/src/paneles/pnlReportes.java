    public void limpiar(){
        fechaInicial.setDate(null);
        fechaFinal.setDate(null);
        cbTipoReporte.setSelectedIndex(0);
        DefaultTableModel modelo = (DefaultTableModel) tblReporte.getModel();
        while(modelo.getRowCount()>0)modelo.removeRow(0);
    }
    
    public void validarReporte(int b){ 
        if(b == 1){
            if(selecArchivo.showDialog(null, "Exportar") == JFileChooser.APPROVE_OPTION){
                archivo = selecArchivo.getSelectedFile();
                archivo = new File(archivo.toString()+".csv");
                ctrl.exportarReporteGeneral(this, archivo, 1); 
            }
        }else if(b == 2){
            if(selecArchivo.showDialog(null, "Exportar") == JFileChooser.APPROVE_OPTION){
                archivo = selecArchivo.getSelectedFile();
                archivo = new File(archivo.toString()+".csv");
                ctrl.exportarReporteGeneral(this, archivo, 2); 
            }
        }else{
            if(selecArchivo.showDialog(null, "Exportar") == JFileChooser.APPROVE_OPTION){
                archivo = selecArchivo.getSelectedFile();
                archivo = new File(archivo.toString()+".csv");
                ctrl.exportarReporteEmpleado(this, archivo);
            }
        }        
    }
    
    
    
