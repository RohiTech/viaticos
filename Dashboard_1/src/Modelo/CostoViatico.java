public class CostoViatico {
    private String id_viatico;
    private String nombre;
    private String valor;

    public String getId_viatico() {
        return id_viatico;
    }

    public void setId_viatico(String id_viatico) {
        this.id_viatico = id_viatico;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }
    
    
}
