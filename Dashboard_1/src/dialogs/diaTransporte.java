    private void tablaTransporteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaTransporteMouseClicked
        int fila = tablaTransporte.rowAtPoint(evt.getPoint());
        int columna = tablaTransporte.columnAtPoint(evt.getPoint());                
        if(columna == 3){
            validarSeleccionPeaje(fila);
        }
    }//GEN-LAST:event_tablaTransporteMouseClicked
    
    
    private void validarSeleccionPeaje(int fila) {
        Gasolina gas = new Gasolina();        
        gas.setId_gasolina(tablaTransporte.getValueAt(fila, 0).toString());
        gas.setDescripcion(tablaTransporte.getValueAt(fila, 1).toString());
        gas.setValor(Integer.parseInt(tablaTransporte.getValueAt(fila, 2).toString()));      
        diaEditGasolina dialog = new diaEditGasolina(new java.awt.Frame(), true, gas, this);
        dialog.setVisible(true);
    }


