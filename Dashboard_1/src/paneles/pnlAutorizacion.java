    private void validarSeleccionEditar(int fila) {
        Autorizacion auto = new Autorizacion();
        auto.setId_autorizacion(tablaAutorizacion.getValueAt(fila, 0).toString());
        auto.setJustificacion(tablaAutorizacion.getValueAt(fila, 3).toString());
        auto.setValor(tablaAutorizacion.getValueAt(fila, 4).toString());
        diaEditAutorizacion dialog = new diaEditAutorizacion(new java.awt.Frame(), true, auto, this); 
        dialog.setVisible(true);
    }
    
