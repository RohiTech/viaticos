    private void validarSeleccion(int fila) {
       String obs = tblReporte.getValueAt(fila, 14).toString();
       String just = tblReporte.getValueAt(fila, 15).toString();
       
       if(obs == null){
           obs = "";
       }
       if(just == null){
           just = "";
       }
       StringTokenizer cant = new StringTokenizer(just, " ");
       String obsver = organizarFrase(obs);
       String justver = organizarFrase(just);       
       if(obs == "" && cant.countTokens() == 0){
           JOptionPane.showMessageDialog(null, "No tiene observaciones");           
       }else{
           JOptionPane.showMessageDialog(null, obsver +"\n"+justver);
       }
        
    }
    public String organizarFrase(String cadena){
        String words[] = cadena.split(" ");
        int numPal = words.length;
        String cad = "";
        int c = 4;
        if(numPal > 0){
           for(int i = 0; i<numPal; i++){
               if(i == c){
                   cad += words[i]+"\n ";
                   c += 4;
               }else{
                   cad += words[i]+" ";
               }
           }
        }
       return cad;
    }
    
