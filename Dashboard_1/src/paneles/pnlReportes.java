    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        TipoDoc tipo = (TipoDoc) cbTipoReporte.getSelectedItem();
        Empleado empleado = (Empleado) cbEmpleado.getSelectedItem();
        
        if(fechaInicial.getDate() == null){
            JOptionPane.showMessageDialog(null, "Ingrese una fecha inicial.");
        }if(fechaFinal.getDate() == null){
            JOptionPane.showMessageDialog(null, "Ingrese una fecha final.");
        }else if(tipo.getIdTipoDoc().equals("0")){
            JOptionPane.showMessageDialog(null, "Seleccione un tipo de reporte");
        }else{
            if(tipo.getIdTipoDoc().equals("1")){
                ctrl.consultarReporteGeneral(this, 1);
            }
            if(tipo.getIdTipoDoc().equals("3")){
                ctrl.consultarReporteGeneral(this, 3);
            }
            if(tipo.getIdTipoDoc().equals("2")){
                if(empleado.getId().equals("0")){
                    JOptionPane.showMessageDialog(null, "Seleccione un empleado");
                }else{
                    ctrl.consultaReporteEmpleado(this);
                }                
            }
        }        
    }//GEN-LAST:event_btnBuscarActionPerformed

