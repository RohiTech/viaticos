    private void validarSeleccion(int fila){
        DecimalFormat formato = new DecimalFormat("#,###");
        CtrlViaticos ctrl = new CtrlViaticos();
        ConsultaViaticos modViatico = new ConsultaViaticos();
        Viatico viatico = new Viatico();
        viatico.setId_tecnico(tablaViaticos.getValueAt(fila, 1).toString());
        viatico.setDestino(tablaViaticos.getValueAt(fila, 2).toString());
        viatico.setValor(Integer.parseInt(tablaViaticos.getValueAt(fila, 8).toString()));
        viatico.setId_viatico(Integer.parseInt(tablaViaticos.getValueAt(fila, 10).toString()));
        
        if(!tablaViaticos.getValueAt(fila, 9).toString().equals("") && !tablaViaticos.getValueAt(fila, 9).toString().equals("Pendiente huella")){
            String mensaje = "Desea registrar como entregado pero \npendiente por registrar huella el viático de: \n";
            mensaje += viatico.getId_tecnico()+"\n";
            mensaje += "Con destino: "+viatico.getDestino()+"\n";
            mensaje += "Y con valor de: $"+formato.format(viatico.getValor())+"\n";
            String estado = "4";
            
            int resp = JOptionPane.showConfirmDialog(null, mensaje, "Mensaje de confirmación", JOptionPane.YES_NO_OPTION);
            switch(resp){
                case JOptionPane.YES_OPTION:
                    ctrl.cancelarViatico(Integer.parseInt(tablaViaticos.getValueAt(fila, 10).toString()), 4, this, modViatico, "1");
                    ctrl.cambiarEstadoAutorizacion(tablaViaticos.getValueAt(fila, 11).toString(), estado );
                break;
                case JOptionPane.NO_OPTION:

                break;        
            }
        }
        
    }
 

