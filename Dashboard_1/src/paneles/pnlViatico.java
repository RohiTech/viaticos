    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Empleado emp = (Empleado) cbEmpleado.getSelectedItem();
        Dependencia dep = (Dependencia) cbDependencias.getSelectedItem();
                
        if(dep.getId_dependencia().equals("0")){
            JOptionPane.showMessageDialog(null, "Seleccione una dependencia");
        }else if(emp.getId().equals("0")){
            JOptionPane.showMessageDialog(null, "Seleccione un empleado");
        }else if(txtFecha.getDate() == null){
            JOptionPane.showMessageDialog(null, "Ingrese una fecha");
        }else{
            Date fecha = txtFecha.getDate();        
            String strDateFormat = "yyyy-MM-dd"; // El formato de fecha está especificado  
            SimpleDateFormat f = new SimpleDateFormat(strDateFormat);        
            String fecha_ingresada = f.format(fecha);
            ctrl.listarViaticosEntregados(tblDevoluciones, emp.getId(), fecha_ingresada);
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

