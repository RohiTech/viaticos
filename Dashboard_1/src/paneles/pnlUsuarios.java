    private void tablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaUsuariosMouseClicked
        int fila = tablaUsuarios.rowAtPoint(evt.getPoint());
        int columna = tablaUsuarios.columnAtPoint(evt.getPoint());
        
        if(columna == 10){
            validarSeleccion(fila);
        }
        if(columna == 9){
            validarPermisos(fila);
        }
    }//GEN-LAST:event_tablaUsuariosMouseClicked

    private void validarSeleccion(int fila) {
        User user = new User();
        user.setId(tablaUsuarios.getValueAt(fila, 1).toString());
        user.setNombre(tablaUsuarios.getValueAt(fila, 2).toString());
        user.setTelefono(tablaUsuarios.getValueAt(fila, 3).toString());
        user.setDireccion(tablaUsuarios.getValueAt(fila, 4).toString());
        user.setCorreo(tablaUsuarios.getValueAt(fila, 5).toString());
        user.setUsername(tablaUsuarios.getValueAt(fila, 7).toString());
        user.setPassword(tablaUsuarios.getValueAt(fila, 8).toString());
        diaEditUsuario dialog = new diaEditUsuario(new java.awt.Frame(), true, user, this);
        dialog.setVisible(true);
    }
    
