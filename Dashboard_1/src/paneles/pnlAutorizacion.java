    private void tablaAutorizacionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaAutorizacionMouseClicked
        int fila = tablaAutorizacion.rowAtPoint(evt.getPoint());
        int columna = tablaAutorizacion.columnAtPoint(evt.getPoint());
        
        if(columna == 5){
            //JOptionPane.showMessageDialog(null,"Bien");
            validarSeleccionEditar(fila);
        }
        if(columna == 6){
            //JOptionPane.showMessageDialog(null,"Bien");
            ctrl.validarSeleccionAnular(fila, this);
        }
    }//GEN-LAST:event_tablaAutorizacionMouseClicked

