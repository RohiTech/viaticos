    private void tblDevolucionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDevolucionesMouseClicked
        int fila = tblDevoluciones.rowAtPoint(evt.getPoint());
        int columna = tblDevoluciones.columnAtPoint(evt.getPoint());                
        if(columna == 7){
            validarSeleccionPeaje(fila);
        }
    }//GEN-LAST:event_tblDevolucionesMouseClicked

    private void validarSeleccionPeaje(int fila) {
        String valor = ctrl.verificarRegistroDevolucion(tblDevoluciones.getValueAt(fila, 8).toString());
        if(valor != null){
            JOptionPane.showMessageDialog(null, "El item seleccionado ya tiene registrado \nuna devolución por el valor de $"+valor);
        }else{            
            Empleado emp = (Empleado) cbEmpleado.getSelectedItem();
            Date fecha = txtFecha.getDate();        
            String strDateFormat = "yyyy-MM-dd"; // El formato de fecha está especificado  
            SimpleDateFormat f = new SimpleDateFormat(strDateFormat);        
            String fecha_ingresada = f.format(fecha);
            Viatico viatico = new Viatico();
            viatico.setId_viatico(Integer.parseInt(tblDevoluciones.getValueAt(fila, 8).toString()));
            viatico.setFechaSalida(tblDevoluciones.getValueAt(fila, 3).toString());
            viatico.setFechaRegreso(tblDevoluciones.getValueAt(fila, 4).toString());
            viatico.setValor(Integer.parseInt(tblDevoluciones.getValueAt(fila, 6).toString()));
            viatico.setFechaProgramada(fecha_ingresada);
            viatico.setId_tecnico(emp.getId());

            diaDevolucionViatico dialog = new diaDevolucionViatico(new java.awt.Frame(), true, viatico, this); 
            dialog.setVisible(true);
        }
    }
    
    public void limpiar(){
        cbDependencias.setSelectedIndex(0);
        txtFecha.setDate(null);
        DefaultTableModel modelo = (DefaultTableModel) tblDevoluciones.getModel();
        while(modelo.getRowCount()>0)modelo.removeRow(0);
    }

